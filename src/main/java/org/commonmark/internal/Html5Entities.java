    private static final Pattern ENTITY_PATTERN = Pattern.compile("^&#[Xx]?");

    public static String entityToString(String input) {
        Matcher matcher = ENTITY_PATTERN.matcher(input);

        if (matcher.find()) {
            int base = matcher.end() == 2 ? 10 : 16;
            try {
                int codePoint = Integer.parseInt(input.substring(matcher.end(), input.length() - 1), base);
                return new String(Character.toChars(codePoint));
            } catch (IllegalArgumentException e) {
                return "\uFFFD";
            }
        } else {
            String name = input.substring(1, input.length() - 1);
            String s1 = namedEntityToString1(name);
            if (s1 != null) {
                return s1;
            }
            String s2 = namedEntityToString2(name);
            if (s2 != null) {
                return s2;
            }
            return input;
        }
    }

